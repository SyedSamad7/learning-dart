void main() {
  // Numbers comes in two sub types (1) [int], (2) [double]
  int a = 10;
  double b = 10.0;
  print('int value $a');
  print('double value $b');

  //we can declare variable with num type. but in this we can pass both value [int] and double
  num c = 10;
  c += 10.0;
  print('num value $c');

  assert('1' == '2');
}
